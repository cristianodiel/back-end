let resultado = document.getElementById("resultado");

function soma(a,b){
    resultado.innerText = parseInt(a) + parseInt(b);
}
function subtracao(a,b){
    resultado.innerText = parseInt(a) - parseInt(b);
}
function multiplicacao(a,b){
    resultado.innerText = parseInt(a) * parseInt(b);
}
function divisao(a,b){
    resultado.innerText = parseInt(a) / parseInt(b);
}



